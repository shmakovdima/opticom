export default function pageData(state = initialState,action) {
  switch (action.type) {
    case 'SET_LOVE':
      return {...state, lovedItems: action.payload}
    case 'SET_WINDOWWIDTH':
      return {...state, windowWidth: action.payload}
    case 'SET_WINDOWTOP':
      return {...state, windowTop: action.payload}
    default:
      return state;
  }
}